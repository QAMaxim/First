#Задание №1
x = input("Введите строку: ")
y = x[::-1]                             #использую срез строки с шагом -1 для обратного порядка
print('Ваша строка в "перевернутом" виде:', y)
#Задание №2
x = input("Введите строку: ")
y = 0                                   #задал переменную для букв
z = 0                                   #задал переменную для чисел
for str in x:                           #запускаю цикл для вычисления цифр и букв используя изученные методы строк
    if str.isalpha():
        y += 1                          #и присваиваю новые значения соответствующей переменной
    elif str.isdigit():
        z += 1
print("Количество букв в Вашей строке:", y)
print("Количество цифр в Вашей строке:", z)
#Задание №3
