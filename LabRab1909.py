str = x.find (y)
while str != -1:              #запускаю цикл для подсчета количества символа до конца строки
    c += 1
    str = x.find(y, str + 1)
print("Символ '{}' встречается в Вашей строке {} раз(а).".format(y, c)) #использую метод format для удобства вывода
#Задание №4
x = input("Введите строку: ")
y = input("Введите слово для поиска: ")
c = x.count (y)                         #ввожу переменную и присваиваю ей значение с использованием метода строки
print("Слово '{}' встречается в Вашей строке {} раз(а).".format(y, c)) #использую метод format для удобства вывода
#Задание №5
